
public class Point {
	
//  ----------------------------------------------------------------------
//  >> 변수
	int x;
	int y;
	
//  ----------------------------------------------------------------------
//  >> 메서드
	@Override
	public String toString() {
		return "x = " + this.x + " y = " + this.y;
	}

//  ----------------------------------------------------------------------
//  >> 생성자
	Point() {super();}
	
	Point(int x, int y) {
		this();
		this.x = x;
		this.y = y;
	}
}
